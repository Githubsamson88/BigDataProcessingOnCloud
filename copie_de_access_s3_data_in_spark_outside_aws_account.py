    plt.xlabel("Nb composantes")
    plt.ylabel("% variance")
    plt.show(block=False)

    def nb_comp():
        for i in range(len(variance)):
            if variance.cumsum()[i] >= 0.95:
                print("{} composantes principales expliquent au moins 95% de la variance totale".format(i + 1))
                return i + 1

    k = nb_comp()
