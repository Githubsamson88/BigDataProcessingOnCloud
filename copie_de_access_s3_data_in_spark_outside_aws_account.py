def recherche_nb_composante(dataframe, nb_comp=400):

    pca = PCA(k=nb_comp, inputCol="features_scaled", outputCol="features_pca")
    model_pca = pca.fit(dataframe)
    variance = model_pca.explainedVariance

    # visuel
